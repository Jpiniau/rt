#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (n == 0)
		return (0);
	while (*s1 == *s2 && s1++ && s2++ && n--)
		if ((*s1 == '\0' && *s2 == '\0') || n == 0)
			return (0);
	if (*s1 == '\0')
		return (-1);
	else if (*s2 == '\0')
		return (1);
	return (*s1 - *s2);
}
