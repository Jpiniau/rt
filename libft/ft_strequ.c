#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if (s1 == NULL && s2 == NULL)
		return (1);
	else if (!s1 || !s2)
		return (0);
	else
		return (!ft_strcmp(s1, s2));
}
